const CLOUDINARY_CLOUD_NAME = 'jasper-jsi-furniture';
const CLOUDINARY_UPLOAD_BASE = `https://res.cloudinary.com/${CLOUDINARY_CLOUD_NAME}/image/upload`;
const SAMPLE_SWATCH_TRANSFORM = 'c_fill,g_auto,w_700,h_700,f_auto,q_auto';

const swatchUrl = (publicId) => `${CLOUDINARY_UPLOAD_BASE}/${SAMPLE_SWATCH_TRANSFORM}/${publicId}.jpg`;
const sourceUrl = (publicId) => `${CLOUDINARY_UPLOAD_BASE}/${publicId}.jpg`;

const TFL_WOODGRAIN_CODES = new Set(['FLO', 'MCH', 'OBK', 'PIN', 'VAL', 'WEA', 'WLH']);
const TFL_STONE_CODES = new Set(['MIN', 'SHD', 'SLG']);

const getFinishType = (code, fallback = 'solid') => {
  if (TFL_WOODGRAIN_CODES.has(code)) return 'woodgrain';
  if (TFL_STONE_CODES.has(code)) return 'stone';
  return fallback;
};

const finish = ({
  category,
  code,
  name,
  publicId,
  finishId,
  cloudinaryFinishCode = code,
  finishType,
  color = '#E5E5E5',
  material,
  substrate,
  collection,
}) => ({
  id: `${category}-${code.toLowerCase()}`,
  name,
  category,
  finishType: finishType || getFinishType(code),
  color,
  image: swatchUrl(publicId),
  sourceUrl: sourceUrl(publicId),
  cloudinaryPublicId: publicId,
  cloudinaryFolder: 'JSI/General Collateral/Material Studio/Finishes/Active_Finishes',
  code,
  finishId,
  cloudinaryFinishCode,
  material,
  substrate,
  collection,
  description: [name, material, substrate].filter(Boolean).join(' - '),
  width: 700,
  height: 700,
  webp: swatchUrl(publicId),
  dataSource: 'cloudinary',
});

